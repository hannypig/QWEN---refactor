def build_prompt(sentence):
    return (
        "You are given four shuffled frames from the same video and a sentence describing the event.\n"
        "Infer the chronological order of the frames.\n"
        "Focus on action progress, pose changes, object position changes, contact state, and before/after states.\n"
        "Do not rely on the input order.\n"
        "Output only the chronological order as a Python list of image numbers.\n"
        "Example output: [3, 1, 2, 4]\n\n"
        f"Sentence: {sentence}"
    )

def make_messages(sentence, num_images=4):
    content = []
    for _ in range(num_images):
        content.append({"type": "image"})
    content.append({"type": "text", "text": build_prompt(sentence)})
    return [{"role": "user", "content": content}]

def move_inputs_to_device(inputs, device):
    out = {}
    for k, v in inputs.items():
        if torch.is_tensor(v):
            out[k] = v.to(device)
        else:
            out[k] = v
    return out

