@torch.inference_mode()
def score_one_candidate(sentence, displayed_image_paths, candidate_display_order):
    """
    candidate_display_order:
        displayed image 번호 기준 chronological order.
        예: (2, 4, 1, 3)
    """

    messages = make_messages(sentence, num_images=4)

    prefix_text = processor.apply_chat_template(
        messages,
        tokenize=False,
        add_generation_prompt=True,
    )

    pil_images = load_pil_images(displayed_image_paths)

    prefix_inputs = processor(
        text=[prefix_text],
        images=pil_images,
        videos=None,
        padding=True,
        return_tensors="pt",
    )

    target_text = answer_to_str(candidate_display_order)

    target_ids = processor.tokenizer(
        target_text,
        add_special_tokens=False,
        return_tensors="pt",
    ).input_ids

    input_ids_cpu = torch.cat([prefix_inputs["input_ids"], target_ids], dim=1)
    attention_mask_cpu = torch.cat(
        [
            prefix_inputs["attention_mask"],
            torch.ones_like(target_ids),
        ],
        dim=1,
    )

    labels_cpu = torch.full_like(input_ids_cpu, -100)
    labels_cpu[:, -target_ids.shape[1]:] = target_ids

    model_inputs = dict(prefix_inputs)
    model_inputs["input_ids"] = input_ids_cpu
    model_inputs["attention_mask"] = attention_mask_cpu

    model_inputs = move_inputs_to_device(model_inputs, MODEL_DEVICE)
    labels = labels_cpu.to(MODEL_DEVICE)
    input_ids = input_ids_cpu.to(MODEL_DEVICE)

    outputs = model(**model_inputs)
    logits = outputs.logits

    shift_logits = logits[:, :-1, :].contiguous()
    shift_labels = labels[:, 1:].contiguous()
    shift_targets = input_ids[:, 1:].contiguous()

    mask = shift_labels.ne(-100)

    log_probs = F.log_softmax(shift_logits.float(), dim=-1)
    selected = log_probs.gather(-1, shift_targets.unsqueeze(-1)).squeeze(-1)

    token_logprob_sum = selected[mask].sum().item()
    token_count = int(mask.sum().item())
    avg_logprob = token_logprob_sum / max(token_count, 1)

    del outputs, logits, shift_logits, shift_labels, shift_targets, log_probs, selected
    if torch.cuda.is_available():
        torch.cuda.empty_cache()

    return avg_logprob

def predict_row(row, image_root: Path):
    sentence = str(row["Sentence"])
    original_paths = image_paths_for_row(row, image_root)

    agg_scores = defaultdict(list)

    for input_perm in TTA_PERMS:
        # input_perm: displayed position -> original input index
        displayed_paths = [original_paths[i - 1] for i in input_perm]

        for cand_display_order in PERMS:
            # candidate displayed-label order -> original Input-index order
            cand_original_order = tuple(input_perm[pos - 1] for pos in cand_display_order)

            score = score_one_candidate(
                sentence=sentence,
                displayed_image_paths=displayed_paths,
                candidate_display_order=cand_display_order,
            )

            agg_scores[cand_original_order].append(score)

    mean_scores = {
        order: float(sum(scores) / len(scores))
        for order, scores in agg_scores.items()
    }

    best_order = max(mean_scores.items(), key=lambda x: x[1])[0]
    best_order = list(best_order)
    best_rank = order_to_rank(best_order)

    top5 = sorted(mean_scores.items(), key=lambda x: x[1], reverse=True)[:5]
    return best_order, best_rank, top5

