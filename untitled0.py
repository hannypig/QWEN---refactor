PERMS = list(itertools.permutations([1, 2, 3, 4]))

def parse_answer(x):
    vals = list(map(int, re.findall(r"\d+", str(x))))
    if len(vals) != 4 or sorted(vals) != [1, 2, 3, 4]:
        raise ValueError(f"Invalid answer: {x}")
    return vals

def order_to_rank(order):
    """
    chronological order of original Input indices -> competition Answer format.
    Example:
        order [4,1,3,2] -> rank [2,4,3,1]
    """
    order = list(map(int, order))
    rank = [None] * 4
    for temporal_pos, input_idx in enumerate(order, start=1):
        rank[input_idx - 1] = temporal_pos
    return rank

def answer_to_str(vals):
    return "[" + ", ".join(map(str, map(int, vals))) + "]"

def is_perm(vals):
    vals = list(map(int, vals))
    return len(vals) == 4 and sorted(vals) == [1, 2, 3, 4]

assert order_to_rank([4, 1, 3, 2]) == [2, 4, 3, 1]
